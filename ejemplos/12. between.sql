SELECT * FROM TEMPLE

--SACAR TODOS LOS EMPLEADOS CON SUELDO ENTRE 1000 Y 2000
SELECT * FROM TEMPLE WHERE SALAR BETWEEN 1000 AND 2000

--ES LO MISMO QUE UTILIZAR AND
SELECT * FROM TEMPLE WHERE SALAR >= 1000 AND SALAR <= 2000

--AHORA SACAR LOS SUELDOS DE LOS EMPLEADOS QUE NO ESTAN ENTRE 1000 Y 2000
SELECT * FROM TEMPLE WHERE SALAR NOT BETWEEN 1000 AND 2000

--LO MISMO CON AND
SELECT * FROM TEMPLE WHERE SALAR < 1000 OR SALAR > 2000